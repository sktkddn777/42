#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	size_t	i;
	size_t	tmp;

	i = 0;
	if (!src && !dst)
		return (dst);
	if (dst < src)
	{
		while (i < len)
		{
			((unsigned char *)dst)[i] = ((unsigned char *)src)[i];
			i++;
		}
	}
	else
	{
		while (i < len)
		{
			tmp = len - i - 1;
			((unsigned char *)dst)[tmp] = ((unsigned char *)src)[tmp];
			i++;
		}
	}
	return (dst);
}
